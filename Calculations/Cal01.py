import math
# addition
print(3+5)          #8
print(3+5.2)        #8.2

# subtraction
print(10-2)         #8
print(2-10)         #-8

# multiplication
print(2*3)          #6
print(3*3.14)       #9.42
print(2*math.pi)    #6.283185307179586

# multiplication with round up  
print(round(2*math.pi, 2))  #6.28

# division
print(4/2)          #2.0
print(5/2)          #2.5
print(5//2)         #2
